    public ArrayList<Line>readWallList(){
        try {
            ObjectInputStream reader = new ObjectInputStream(new FileInputStream("src/pacmanx/maps lines/" + mapName + ".abc"));
            return (ArrayList<Line>) reader.readObject();
        } catch (Exception ex) {
            Logger.getLogger(Area.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return null;
    }
