    public BufferedImage getMap() {
        try {
            return ImageIO.read(new File("src/pacmanx/maps/" + mapName + "jpg"));
        } catch (IOException ex) {
            Logger.getLogger(Area.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    
